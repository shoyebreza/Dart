// Dart program to show the Sets concept
void main()
{
// Declaring set in First Way
var gfg1 = <String>{'Shoyeb'};

// Printing First Set
print("Output of first set: $gfg1");

// Declaring set in Second Way
Set<String> gfg2 = {'reza'};

// Printing Second Set
print("Output of second set: $gfg2");
}
